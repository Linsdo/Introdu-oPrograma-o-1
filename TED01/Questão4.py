valor1 = float(input('Insira um número: '))
valor2 = float(input('Insira outro número: '))
valor3 = float(input('Insira outro número: '))

soma = valor2 + valor3

if valor1 > soma:
    print('O primeiro valor é maior que a soma dos outros dois números.')
else:
    print('O primeiro valor não é maior que a soma dos outros dois números.')